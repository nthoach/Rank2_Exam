#include <unistd.h>

int	main(int agc, char **agv)
{
	int	i = 0;
	int	flag = 0;

	if (agc == 2)
	{
		while (agv[1][i])
		{
			if (flag == 1 && (agv[1][i] == ' ' || agv[1][i] == '\t')
				&& (agv[1][i+1]))
			{
				write(1, "   ", 3);
				flag = 0;
			}
			if (agv[1][i] != ' ' && agv[1][i] != '\t')
			{
				write(1, &agv[1][i], 1);
				flag = 1;
			}
			i++;			
		}
	}
	write(1, "\n", 1);
}