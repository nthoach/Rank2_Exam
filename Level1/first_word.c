#include <unistd.h>

int	main(int agc, char **agv)
{
	int	i;

	i = 0;
	if (agc == 2)
	{
		while (agv[1][i] == ' ' || agv[1][i] == '\t')
		i++;
		while (agv[1][i] && agv[1][i] != '\t' && agv[1][i]!= ' ')
		{
			write(1, &agv[1][i], 1);
			i++;	
		}
	}
	write(1, "\n", 1);	
	return (0);	
}
