#include <unistd.h>

int	main(int agc, char **agv)
{
	int		i;
	char	c;
	i = 0;
	if (agc == 4)
	{
		
		
		while (agv[1][i] && agv[2][1] == 0 &&
				agv[3][1] == 0 && agv[2][0] && agv[3][0])
		{
			if (agv[1][i] == agv[2][0])
				c = agv[3][0];
			else
				c = agv[1][i];
			write(1, &c, 1);			
			i++;
		}		
	}
	write(1, "\n", 1);
	return (0);
}