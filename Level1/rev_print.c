#include <unistd.h>

int	main(int agc, char **agv)
{
	int	i;

	i = 0;
	if (agc == 2)
	{
		while (agv[1][i])
			i++;
		while (i)
		{
			i--;
			write(1, &agv[1][i], 1);	
		}

	}
	write(1, "\n", 1);
}